// Scans public/products/<slug>/ for image files and regenerates lib/product-images.ts,
// a slug -> [image paths] map used by the catalog. Run with `npm run images` after
// adding or changing product photos.
//
// Convention:
//   public/products/<product-slug>/anything.jpg   -> that product's images (sorted by filename)
// Supported extensions: .jpg .jpeg .png .webp .avif .gif

import { readdirSync, statSync, writeFileSync, existsSync } from "node:fs";
import { join, extname } from "node:path";

const ROOT = process.cwd();
const PRODUCTS_DIR = join(ROOT, "public", "products");
const OUT_FILE = join(ROOT, "lib", "product-images.ts");
const IMAGE_EXT = new Set([".jpg", ".jpeg", ".png", ".webp", ".avif", ".gif"]);

/** Natural sort so "2.jpg" comes before "10.jpg". */
function naturalCompare(a, b) {
  return a.localeCompare(b, undefined, { numeric: true, sensitivity: "base" });
}

function collect() {
  const map = {};
  if (!existsSync(PRODUCTS_DIR)) return map;

  for (const entry of readdirSync(PRODUCTS_DIR)) {
    const slugPath = join(PRODUCTS_DIR, entry);
    if (!statSync(slugPath).isDirectory()) continue;

    const files = readdirSync(slugPath)
      .filter((f) => IMAGE_EXT.has(extname(f).toLowerCase()))
      .sort(naturalCompare)
      .map((f) => `/products/${entry}/${f}`);

    if (files.length > 0) map[entry] = files;
  }
  return map;
}

function render(map) {
  const slugs = Object.keys(map).sort();
  const body = slugs.length
    ? slugs
        .map(
          (slug) =>
            `  ${JSON.stringify(slug)}: [\n${map[slug]
              .map((p) => `    ${JSON.stringify(p)},`)
              .join("\n")}\n  ],`,
        )
        .join("\n")
    : "";

  return `/**
 * AUTO-GENERATED by scripts/build-image-manifest.mjs — safe to commit, don't edit by hand.
 *
 * Maps a product slug to an ordered list of image paths under /public. To add your
 * own photos: drop files into \`public/products/<slug>/\` then run \`npm run images\`.
 * Products without an entry here fall back to keyword placeholder photography.
 */
export const PRODUCT_IMAGES: Record<string, string[]> = {${
    body ? `\n${body}\n` : ""
  }};
`;
}

const map = collect();
writeFileSync(OUT_FILE, render(map), "utf8");

const count = Object.keys(map).length;
const total = Object.values(map).reduce((n, a) => n + a.length, 0);
console.log(
  `Wrote ${OUT_FILE.replace(ROOT + "\\", "").replace(ROOT + "/", "")} — ` +
    `${count} product(s), ${total} image(s).`,
);
